package thirdWeek4thHW.quiz7;

public class Calculator {

	

	
	
	protected int result;
	
	void add(int a) {
		result += a;
	}
	
	void add(int a, int b) {
		result += a;
		result += b;
	}

	void add(int a, int b, int c) {
		result += a;
		result += b;
		result += c;
	}
	
	void add(int a, int b, int c, int d) {
		result += a;
		result += b;
		result += c;
		result += d;
	}
	
}
	

